// @lc code=start
class Solution {
    public int removeElement(int[] nums, int val) {
        int slow = 0;
        //区间[0,left)中的元素都不等于val
        for (int fast = 0;fast < nums.length; fast++) {
            if (nums[fast] != val) {
                nums[slow++] = nums[fast];
            }
        }
        return slow;
    }
}
